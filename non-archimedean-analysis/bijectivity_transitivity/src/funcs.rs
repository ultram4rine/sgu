pub fn f(x: i32) -> i32 {
    -71 * x - (x * x)
}

pub fn g(x: i32) -> i32 {
    5 * x + 16
}

pub fn f_256(x: i32) -> i32 {
    (x ^ 1)
        ^ (2 * (x
            & (1 + 2 * x)
            & (3 + 4 * x)
            & (7 + 8 * x)
            & (15 + 16 * x)
            & (31 + 32 * x)
            & (63 + 64 * x)))
        ^ (4 * (x * x + 8))
}
